int adj_product(char in[], int adjacent, int size)
{
  int product = 0;
  int test_product;
  int i;
  int j;
  for(i = 0; i <= size - adjacent; i++)
    {
      test_product = 1;
      j = i;
      while(j - i < adjacent)
	{
	  test_product *= (int) (in[j] - '0');
	  if(in[j] == '0')
	    break;
	  j++;
	}
      if(test_product > product)
	product = test_product;
    }
  return product;
}

